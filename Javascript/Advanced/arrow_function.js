// declaring of arrow function
// syntax: declaring_variable = (no of parameter) =>{body of the function}
const k = (x,y) =>{
    return x*y;
}
console.log(k(5,6));
// Normal function declaring in js
function j(x,y){
    return x+y;
}
console.log(j(8,9));

